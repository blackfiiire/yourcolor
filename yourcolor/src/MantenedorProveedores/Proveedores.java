    private void btnBusquedaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBusquedaActionPerformed
        Limpiar();
        DefaultTableModel m = (DefaultTableModel) jTable1.getModel();
        try{
            //abrir conexion
            conexion.conectar();
            //Consulta a ejecutar
            String     sql = ("SELECT rut, nombre, fono, email, direccion FROM proveedores WHERE (rut like '%"+tfBusqueda.getText()+"%') or (nombre like '%"+tfBusqueda.getText()+"%') or (direccion like '%"+tfBusqueda.getText()+"%')");
            //Preparar la consulta
            Statement st = cn.createStatement();
            //Ejecutar consulta
            ResultSet rs = st.executeQuery(sql);
            //Mostrar los datos
            while(rs.next()){
                String rut = rs.getString("rut");
                String nombre = rs.getString("nombre");
                String direccion = rs.getString("direccion");
                String fono = rs.getString("fono");
                String email = rs.getString("email");
                Object fila[] = {rut, nombre, fono, email, direccion};
                m.addRow(fila);
            }
        }catch(Exception e){
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnBusquedaActionPerformed

