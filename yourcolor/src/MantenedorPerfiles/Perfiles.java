    //Metodo Borrar
    public void Borrar(){
        try {
            //tomar modelo de la tabla
            DefaultTableModel m = (DefaultTableModel) jTable1.getModel();
            //Seleccionar Fila
            int IdSelect = jTable1.getSelectedRow();
            String id = m.getValueAt(IdSelect, 0).toString();
            //abrir conexion
            conexion.conectar();
            //query
            String Query = "DELETE FROM perfiles WHERE (id = '" + id + "')";
            Statement st = cn.createStatement();
            st.execute(Query);
            //cerrar conexion
            conexion.desconectar();        
        } catch (SQLException ex) {
            Logger.getLogger(Perfiles.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
