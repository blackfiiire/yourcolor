    //metodo para cargar datos
    public void Cargar(){
            try {
                    conexion.conectar();
                    //query
                    String consulta = "SELECT * FROM perfiles";
                    Statement st2 = cn.createStatement();
                    ResultSet rs2 = st2.executeQuery(consulta);
                    if(rs2.next()==true)
                    {
                        String id = rs2.getString("id");
                        String descrip = rs2.getString("descripcion");

                        DefaultTableModel m = (DefaultTableModel) jTable1.getModel();
                        Object fila[] = {id,descrip};
                        m.addRow(fila);
                    }
                    conexion.desconectar();
            } catch (SQLException ex) {
                Logger.getLogger(Perfiles.class.getName()).log(Level.SEVERE, null, ex);
            }   
    }
    
    //METODO AGREGAR
    public void Agregar(){
        try {
            //abrir nueva conexion
            conexion.conectar();
            String n = "NULL";
            //armar query
            String Query = "INSERT INTO perfiles (id,descripcion) values (" + n + ",'" + this.tfDetalle.getText() + "');";
            //ejecutar query
            Statement st1 = cn.createStatement();
            st1.executeUpdate(Query);
            //cerrar conexion
            conexion.desconectar();
        } catch (SQLException ex) {
            Logger.getLogger(Perfiles.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
